class Movie():

	def __init__(self, movie_title, movie_plot, movie_image, movie_video):
		self.movie_name = movie_title,
		self.movie_story = movie_plot,
		self.movie_art = movie_image,
		self.movie_youtube = movie_video 

	def show_movie_variables(self):
		print(self.movie_name)
		print(self.movie_story)
		print(self.movie_art)
		print(self.movie_youtube)


